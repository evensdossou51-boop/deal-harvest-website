// Helper function to filter deals
export const filterDeals = (deals, category, store, search) => {
  return deals.filter(deal => {
    const matchesCategory = category === "all" || deal.category === category;
    const matchesStore = store === "all" || deal.store === store;
    const matchesSearch = !search || 
      deal.name.toLowerCase().includes(search.toLowerCase()) ||
      deal.category.toLowerCase().includes(search.toLowerCase()) ||
      deal.store.toLowerCase().includes(search.toLowerCase());
    
    return matchesCategory && matchesStore && matchesSearch;
  });
};